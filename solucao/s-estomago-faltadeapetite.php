                <p> <h1  class="submenu"> Causas:</h1>
                    <p>
                        A falta de apetite é um sintoma que está presente na maioria das doenças dos cães. Diferentemente dos seres humanos, que mesmo adoentados ainda se forçam a comer, os cães, quando não estão se sentindo bem, não ingerem de jeito nenhum o alimento.
                        <br>-Anorexia: é quando o animal não ingere nenhum tipo de alimento, apenas cheira e se afasta. Já a inapetência, é quando o animal diminui o consumo de alimento, ou seja, não consome a mesma quantidade de antes.
                       <br>-Inapetência: quando o animal consome a mesma ração durante muito tempo. Nesse caso trocar a ração pode ser uma boa opção.
                        <br>A origem é multifatorial para a falta de apetite em cães. Na maioria dos casos é devido a doenças virais comuns em cães, como: Cinomose, Parvovirose, Coronavirose e etc. Outra doença que na sua fase inicial pode causar falta de apetite é a Ehrlichiose (Doença do Carrapato). Outras moléstias levam o cão à ausência de apetite, tais como: Problemas gástricos, hepáticos, dentários e até mesmo intoxicação.
                        <br>Nos animais que apresentam uma falta de apetite, normalmente são observados outros sinais clínicos. Na grande maioria, juntamente com a anorexia, vem a relutância da ingestão de líquidos. O vômito e a diarreia podem ser encontrados, porém, nesse caso é bem mais perigoso, já que ambos levam o animal rapidamente a um quadro de desidratação severo.
                    </p>
