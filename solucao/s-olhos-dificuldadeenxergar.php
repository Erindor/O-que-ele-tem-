                <p> <h1  class="submenu"> Identifica doença e como tratar. </h1>
                    <p><b>Conjutivite:</b> Se você reparou que o seu cão está com o olho um pouco ou muito avermelhado, ele pode ter sido infectado por uma bactéria ou por um vírus que causam a conjuntivite, que entraram em contato com esta parte do corpo por meio da poluição ou devido a ações climáticas. Além do vermelho nos olhos, a conjuntivite canina apresenta inchaço nos olhos, pálpebras superiores inflamadas, secreção de cor amarela ou esverdeada, lacrimejamento e irritação frequente por causa de pequenas partículas. O tratamento é simples, e envolve a limpeza com soro fisiológico e a aplicação de colírios prescritos pelo veterinário. Se o quadro estiver muito feio, o profissional também pode indicar antibióticos. Enquanto estiver se recuperando, seu pet pode ficar próximo de outros animais, pois a doença não é contagiosa, mas ele não deve coçar os olhos para não piorar a situação, e, se for o caso, ele pode usar um colar elisabetano, o popular cone de pescoço.
                    <p><b>Glaucoma:</b> A doença, que pode cegar se não receber os devidos cuidados, pode ser percebida quando o cão vai ficando com os olhos turvos e esbranquiçados, as pupilas dilatadas e a dificuldade de enxergar. Ela acontece quando ocorre um aumento da pressão sanguínea dentro dos olhos, o que acaba afetando o nervo e a retina do bichinho, principalmente quando ele começa a envelhecer. Na clínica veterinária, o médico vai realizar uma avaliação da pressão intraocular e um exame de fundo do olho para diagnosticar e remediar, geralmente com o uso de colírios por toda a vida do animal. No entanto, se for descoberta logo no início, o glaucoma pode ser revertido com a drenagem do líquido acumulado.
                    <p><b>Catarata:</b> Muito cuidado, pois esse é o principal motivo de cegueira no seu melhor amigo! A doença deixa o cristalino opaco devido à morte das células, formando uma mancha branca no centro do olho do cachorro que bloqueia a passagem de luz e a visão. De origem genética, diabética, traumática ou secundária à degeneração da retina, a catarata precisa ser operada, assim como no homem, e uma prótese pode substituir o tecido natural dos olhos do seu cão. Ela pode se manifestar no nascimento, na juventude e na velhice, principalmente nos animais de pequeno e grande porte, e, se esse for o caso do seu pet, é melhor fazer a castração para que a doença não se espalhe para as próximas gerações.
                      
