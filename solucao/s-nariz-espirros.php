                <p> <h1  class="submenu"> Perguntas que devo fazer: </h1>
                    <b>Será que o seu cão sofre de alergias?</b> As alergias respiratórias são muitas vezes a causa de surtos sazonais de espirros.
                    Se seu cão tem alergias a várias plantas sazonais ou pólen, isso pode explicar os espirros e seu cão pode responder bem a um anti-histamínico. Verifique com seu veterinário para a dosagem e confirme se o seu cão não tem riscos adicionais de saúde específicos que o impeçam de usar os medicamentos com segurança.
                    <p>
                    <b>Seu cão ficou fora de casa ou não foi supervisionado por qualquer período de tempo recentemente?</b>Cães são curiosos e propensos a cheirar novas e interessantes plantas, objetos, e substâncias, algumas delas perigosas. Qualquer uma destas coisas pode ser inalada, resultando em obstrução parcial ou completa de uma passagem nasal, dor, inflamação, e resultando em espirros. Por vezes, mesmo depois dos elementos irritantes não estarem mais presentes, o que causou irritação promoverá continuou espirros episódios. Uma picada de abelha é um exemplo perfeito e que pode continuar a piorar com o tempo, se não tratada. Se em algum momento durante a sua avaliação de espirros do seu cão os sintomas piorarem, leve-o para o seu veterinário imediatamente.
                    <p>
                    <b>Você viu qualquer inchaço ou assimetria no rosto do seu cão?</b> Trauma e picadas de insetos ou ataques de animais são causas comuns de inchaço facial associadas com espirros. Se houver suspeita de trauma por contusão, leve-o ao veterinário. Se uma picada de inseto ou impacto é a sua suspeita, seu veterinário pode recomendar remédios e pomadas para aliviar o problema até que o cão pare de espirrar.
                    <p>
                    <b>Parece haver qualquer sinal de infecção?</b> Qualquer descarga amarelada ou esverdeada do nariz, olhos ou boca poderia explicar os espirros e ser necessário o encaminhamento para o seu veterinário para tratamento médico.
                    <p>
                    <b>Você viu algum sangramento das narinas do seu cão?</b> Embora seja possível que repetidos espirros possa traumatizar passagens nasais do seu cão o suficiente para causar sangramento, já é motivo o suficiente para merecer uma visita ao seu veterinário.
                    <p>
                    <b>Será que o fluxo de ar através das narinas do seu cão parece difícil ou irregular?</b> Inflamação nasal ou obstrução vai resultar em dificuldade em passar ar através dessas aberturas. Se este for o caso, seu veterinário provavelmente recomendará um anti-histamínico. Caso não funcione, pode ser necessária uma intervenção mais séria.
