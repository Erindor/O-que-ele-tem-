                <h1  class="submenu"> Possíveis doenças:</h1>
                    <p> <b>A Insuficiência renal crônica</b> <br/>
                    É devida a lesões glomerulares (glomerulonefrites de origem imunológica, amiloide, nefro-angio-esclerose) 
                    ou a lesões intersticiais, todas provocadas por uma destruição progressiva e irreversível dos nefros, 
                    as unidades funcionais dos rins. À medida que a quantidade de nefros diminui, a função renal fica 
                    comprometida e a insuficiência renal aumenta. </p>
                    
                    <p>O animal que sofre de insuficiência renal crônica começa bebendo e urinando 
                    mais do que o de costume (poliúria/polidipsia). Em seguida aparecem as perturbações 
                    digestivas (vômitos, diarréia), que precedem as neurológicas. </p>
                    
                    <p> <b>A Insuficiência renal aguda</b> <br/>
                    O animal entre de súbido em anexoria, tem vômitos e diarréias, por vezes hemorrágicas. 
                    Paralelamente, a concentração de uréia e creatinina no sangue aumenta, atingindo valores 
                    via de regra muito elevados, ao passo que na insuficiência renal crônica esses mesmos 
                    parâmetros (que se usam para avaliar a função renal) aumentam com moderação. </p>
                    
                    <p>Na origem desta forma de insuficiência renal, podem estar fatores isquêmicos 
                    (caso do rim que tenha sofrido alterações decorrentes de mudanças de circulação sangüínea), 
                    infecciosos (sobretudo leptospirose) e tóxicos. </p>
                    
                    <p> Os tratamentos aplicáveis no caso de insuficiência renal aguda consistem na terapia 
                    com fluidos e eletrólitos e em medidas de purificação extra-renal (diálise peritoneal). 
                    Algumas formas da doença respondem bem à medidas terapêuticas, mas nas outras o prognóstico é, 
                    infelizmente, mais reservado. </p>
                    
                    <p> <b>Cálculos Urinários - Urolitíase</b> <br/>
                    Nos cães podemos encontrar 4 tipos de urólitos (cálculos): os de fosfato, 
                    geralmente associados a uma infecção do trato urinário; os de urato; os de 
                    oxalato decorrentes de alterações metabólicas; e os de cistina, cuja ocorrência 
                    depende de predisposição hereditária. </p>
                    
                    <p> A freqüência e o tipo dos cálculos podem variar conforme as raças. Assim, os 
                    cálculos de cistina são observados com mais freqüência no Dachshund e os de urato no Dálmata.
                    A presença destes cálculos no trato urinário pode levar ao aparecimento de hematúria (sangue na urina), 
                    cistites, incontinência, retenção urinária, complicações infecciosas e renais. 
                    Por vezes um cálculo introduz-se na uretra e não consegue passar por certas zonas 
                    particularmente estreitas. Daí resulta uma obstrução uretral que só poderá ser resolvida, 
                    muitas vezes, recorrendo-se à cirurgia. <p/>
                    
                    <p> O tratamento da urolitíase no cão pode ser medicamentoso ou cirúrgico, 
                    conforme o quadro que o animal apresente. </p>
                    
                    <p>Em casos obstrutivos, o cão pode apresentar-se visivelmente desidratado, 
                    letárgico ou comatoso. Nestes casos a terapia é instituída de modo a estabilizar
                    as condições do animal para que possam ser iniciados os procedimentos para a remoção do cálculo. </p>
                    
                    <p> <b>As infecções do trato urinário (ITU)</b> <br/>
                    Na maior parte dos casos, a infecção urinária canina é uma consequência de infecções em órgãos 
                    vizinhos, como na próstata, útero, vagina ou, mais raramente, sistêmicas. Por conseguinte, 
                    não basta tratar os seus sintomas, tem é de se tratar a sua causa. Esta procura-se sistematicamente 
                    mediante um exame clínico aprofundado com radiografias e exames complementares. </p>
                    
                    <p> As infecções do trato urinário são causadas 
                    por germes que em geral provém do tubo digestivo. Algumas, 
                    principalmente as crônicas, são particularmente difíceis de curar. </p>
                    
                    <p> <b>Incontinência Urinária</b> <br/>
                    Caracterizada pela micção involuntária, a incontinência urinária do cão pode ter múltiplas causas. 
                    Pode ser o resultado de lesões do sistema nervoso, de mal-formações congênitas, de lesões adquiridas 
                    na bexiga e nos esfíncteres ou de desequilíbrios hormonais. Não existe, por isso, um tratamento único 
                    para a incontinência urinária, mas tratamentos específicos de acordo com cada causa. </p>
                    
                    <p>Em geral, as lesões do sistema nervoso, da bexiga e dos esfíncteres são difíceis de tratar e, 
                    embora existam nvos protocolos terapêuticos, os resultados ainda não são satisfatórios.</p>
                    
                    <p>Em contrapartida, algumas mal-formações congênitas podem ser totalmente corrigidas mediante cirurgia. 
                    Assim acontece com a ectopia ureteral ou com a persistência do canal fraco.</p>
                    
                    <p>Como sequela de certas intervenções cirúrgicas, também podem surgir incontinências urinárias 
                    causadas por fístulas ou por aderências. Uma nova intervenção pode recuperar a continência normal, 
                    mas nem sempre o resultado é garantido.</p>

