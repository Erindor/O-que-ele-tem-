                    <p><b>Gastrite:</b>Uma doença muito comum é a gastrite canina. A gastrite é uma inflamação da mucosa que reveste o estômago. A     doença causa dor, incômodo e o primeiro sinal costuma ser o vômito, que pode vir acompanhado ou não de sangue. <p>
                    A gastrite pode ocorrer a partir de uma variedade de causas, incluindo a exposição a certos tipos de vírus e bactérias, ou uma reacção de certos tipos de alimentos. outros exemplos de causas são:<br> 
                    *Alergia alimentar ou intolerância;<br>
                    *Toxinas presentes em fertilizantes, herbicidas e agentes de limpeza;<br>
