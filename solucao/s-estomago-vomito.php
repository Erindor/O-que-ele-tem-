          Normalmente o cão com parvovirose fica muito desidratado e precisa ser internado. Ele precisa receber fluidos e eletrólitos para repor as perdas devido à desidratação. Em casos muito graves, utiliza-se expansores plasmáticos, para que o cão não tenha um choque hipovolêmico. Além disso, o cão começa a tomar antibióticos e remédios para evitar o vômito e não piorar a desidratação.<br>
