                <h1  class="submenu"> Diarreia:</h1>
                <p>A diarreia não é uma doença mas sim uma condição que pode ser causada por várias doenças. Ela representa uma disfunção no processo normal de defecação. A diarreia num cão ou gato é caracterizada pelo aumento da frequência e volume das fezes, assim como diminuição da consistência das mesmas.
                    As fezes diarreicas contêm não só maior quantidade de água e eletrólitos, mas também poderão conter muco, sangue, gordura ou comida não digerida. A diarreia pode ser originária do intestino delgado ou intestino grosso (colite), podendo ser classificada em agúda ou crônica.
                    <p>
                        <b>Diarreia Aguda:</b> Este tipo de diarreia normalmente dura menos de 48 horas, normalmente contem muco, sendo comum encontrar sangue neste tipo de diarreia. O animal normalmente perde o apetite e aumenta a frequência de defecação, assim como a sua urgência para defecar.
                    </p>
                    <p>
                       <b> Diarreia Crônica:</b> Este tipo de diarreia dura 7 a 10 dias ou mais, sendo por vezes muitos escura, devido à presença de sangue digerido nas fezes, não apresentando muco no seu conteúdo.
                    </p>
