                    <p><b>Tratamento:</b> Para tratar o seu animal com diarreia tem de ser determinada a causa. O Médico Veterinário irá realizar um exame clínico ao seu cão ou gato e perguntar-lhe o historial médico, dieta e rotina diária, podendo ser necessária a realização de análises sanguíneas, radiografias ou outros exames de diagnóstico. 
