                  O sangramento na pele dos cães pode ter diversas causas, sendo que há feridas externas, 
                  e internas. 
                  Podemos ver na pele petéquias, equimose, e hematomas , geralmente devido a lesões que 
                  levam ao sangramento (hemorragia) sob a área afetada. Mais especificamente, hematomas 
                  é um prejuízo para a pele, que faz com que os vasos sanguíneos se a ruptura e a 
                  descoloração do tecido, devido à presença de glóbulos vermelhos; petéquias é um 
                  pequeno ponto vermelho ou roxo no corpo causada por hemorragia menor; Esquimose 
                  é uma mancha na pele, de coloração variável, produzida por extravasamento de sangue.
                
                 <p>Apesar de petéquias, equimose, e hematomas ocorrem frequentemente devido a lesões, 
                 a seguir também podem causar ou fazer o cão mais susceptíveis a este tipo de hemorragia 
                 como , trombocitopatia, desordens congênitas ou  
                 adquiridas afetando a capacidade de plaquetas em aderir aos vasos sanguíneos danificados, 
                 doença vascular, Vasculite secundária a infecção, como febre maculosa.</p>
                  <p> Já alguns sangramentos externos podem ser causados por diversas doenças. Muitos dos sangramentos 
                  na pele podem ser causados por alergias a picadas de alguns parasitas, como pulgas e carrapatos. 
                  Isso faz com que o cão se coce mais e acabe se ferindo, causando  sangramento. Sangramentos também 
                  pode significar que o cão esteja com Micose, pois isso também faz com que o cão se coce  muito. 
                  A sarna é outra doença que faz com que o cão acabe se coçando e muitas vezes de ferindo, 
                  fazendo com que a pele sangre. Algumas outras doenças, como a Leishmaniose, fazem com que as 
                  feridas na pele apareçam, e são transmitidas por agentes de transmissão, como os mosquitos.
                </p>
                
